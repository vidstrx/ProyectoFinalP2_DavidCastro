    private boolean verificarUsuario(String nombre, String contrasena){
        boolean isUsuario = true;
        for (Usuario usuario : usuarios) {
            if (nombre.equals(usuario.getNombre()) && contrasena.equals(usuario.getContrasena())){
                isUsuario = true;
                break;
            } else {
                isUsuario = false;
            }
        }
        return isUsuario;
    }

